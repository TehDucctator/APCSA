public class Main {
  public static void main(String[] args) {
    // questions answered in comments
    // the int type represents integers
    // the double type represents decimals
    // the boolean type represents true or false
    // final indicates that it is a constant.
    // It's useful when storing a value that will not change for easy access
    
    int balance = 10; 
    final double price = 1000.25; 
    boolean broke = true;
    
    System.out.print("Balance: ");
    System.out.println(balance);
    System.out.println("Price: " + price);
    System.out.println("Broke? " + broke);
  }
}